const pollTaskStatus = async (taskId) => {
  try {
    const response = await getTaskStatus(taskId)
    
    if (response.success) {
      const task = response.data
      
      // Actualizare afișare progres
      buildProgress.value = {
        progress: task.progress || 0,
        message: task.message || 'Procesare în curs...'
      }
      
      console.log('Task status:', task.status, 'Progress:', task.progress)
      
      if (task.status === 'completed') {
        console.log('✅ Construire graf finalizată, se încarcă datele complete...')
        
        stopPolling()
        stopGraphPolling()
        currentPhase.value = 2
        
        // Actualizare afișare progres为FinalizareStare
        buildProgress.value = {
          progress: 100,
          message: 'Construire finalizată, se încarcă graful...'
        }
        
        // Reîncărcare date proiect pentru graph_id
        const projectResponse = await getProject(currentProjectId.value)
        if (projectResponse.success) {
          projectData.value = projectResponse.data
          
          // Încărcare finală date graf complete
          if (projectResponse.data.graph_id) {
            console.log('📊 Încărcare graf complet:', projectResponse.data.graph_id)
            await loadGraph(projectResponse.data.graph_id)
            console.log('✅ Încărcare graf finalizată')
          }
        }
        
        // Curățare afișare progres
        buildProgress.value = null
      } else if (task.status === 'failed') {
        stopPolling()
        stopGraphPolling()
        error.value = 'Construire graf eșuată: ' + (task.error || 'Eroare necunoscută')
        buildProgress.value = null
      }
    }
  } catch (err) {
    console.error('Poll task error:', err)
  }
}

