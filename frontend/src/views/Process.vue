  selectedItem.value = {
    type: 'edge',
    data: edgeData
  }
}

const getPhaseStatusClass = (phase) => {
  if (currentPhase.value > phase) return 'completed'
  if (currentPhase.value === phase) return 'active'
  return 'pending'
}

const getPhaseStatusText = (phase) => {
  if (currentPhase.value > phase) return 'Finalizat'
  if (currentPhase.value === phase) {
    if (phase === 1 && buildProgress.value) {
      return `${buildProgress.value.progress}%`
    }
    return 'În desfășurare'
  }
  return 'În așteptare'
}

// Inițializare - procesare proiect nou sau încărcare proiect existent
const initProject = async () => {
  const paramProjectId = route.params.projectId
  
  if (paramProjectId === 'new') {
    // Proiect nou: obține datele de încărcat din store
    await handleNewProject()
  } else {
    // Încărcare proiect existent
    currentProjectId.value = paramProjectId
    await loadProject()
  }
}

// Procesare proiect nou - apelare API ontology/generate
const handleNewProject = async () => {
  const pending = getPendingUpload()
  
  if (!pending.isPending || pending.files.length === 0) {
    error.value = 'Nu există fișiere de încărcat, reveniți la pagina principală'
    loading.value = false
    return
  }
  
  try {
    loading.value = true
    currentPhase.value = 0 // Etapă generare ontologie
    ontologyProgress.value = { message: 'Se încarcă fișierul și se analizează documentul...' }
    
    // Construire FormData
    const formDataObj = new FormData()
    pending.files.forEach(file => {
      formDataObj.append('files', file)
    })
    formDataObj.append('simulation_requirement', pending.simulationRequirement)
    
    // Apelare API generare ontologie
    const response = await generateOntology(formDataObj)
    
    if (response.success) {
      // Curățare date de încărcat
      clearPendingUpload()
      
      // Actualizare ID și date proiect
      currentProjectId.value = response.data.project_id
      projectData.value = response.data
      
      // Actualizare URL (fără reîmprospătare pagină)
      router.replace({
        name: 'Process',
        params: { projectId: response.data.project_id }
      })
      
      ontologyProgress.value = null
      
      // Pornire automată construire graf
      await startBuildGraph()
    } else {
      error.value = response.error || 'Generare ontologie eșuată'
    }
  } catch (err) {
    console.error('Handle new project error:', err)
    error.value = 'Inițializare proiect eșuată: ' + (err.message || 'Eroare necunoscută')
  } finally {
    loading.value = false
  }
}

// Încărcare proiect existentDate
