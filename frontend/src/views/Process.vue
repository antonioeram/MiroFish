const graphData = ref(null)
const buildProgress = ref(null)
const ontologyProgress = ref(null) // Progres generare ontologie
const currentPhase = ref(-1) // -1: Upload în curs, 0: Generare ontologie, 1: Construire graf, 2: Finalizat
